.MODEL SMALL

.DATA

EXTRN playerX:BYTE
EXTRN playerY:BYTE

.CODE

EXTRN DRAW_PLAYER:PROC
EXTRN ERASE_PLAYER:PROC
EXTRN QUIT_GAME:PROC

PUBLIC GET_INPUT

GET_INPUT PROC

    MOV AH,01h
    INT 16h
    JZ NO_KEY

    MOV AH,00h
    INT 16h

    CMP AL,'a'
    JE MOVE_LEFT

    CMP AL,'A'
    JE MOVE_LEFT

    CMP AH,4Bh
    JE MOVE_LEFT

    CMP AL,'d'
    JE MOVE_RIGHT

    CMP AL,'D'
    JE MOVE_RIGHT

    CMP AH,4Dh
    JE MOVE_RIGHT

    CMP AL,'q'
    JE EXIT_NOW

    CMP AL,'Q'
    JE EXIT_NOW

    RET

MOVE_LEFT:

    CMP playerX,3
    JLE NO_KEY

    CALL ERASE_PLAYER
    SUB playerX,2
    CALL DRAW_PLAYER

    RET

MOVE_RIGHT:

    CMP playerX,76
    JGE NO_KEY

    CALL ERASE_PLAYER
    ADD playerX,2
    CALL DRAW_PLAYER

    RET

EXIT_NOW:

    JMP QUIT_GAME

NO_KEY:

    RET

GET_INPUT ENDP

END

----------- MUHAMMAD WAQAR -------------------

.MODEL SMALL

.DATA

EXTRN targetX:BYTE
EXTRN targetY:BYTE
EXTRN playerX:BYTE
EXTRN playerY:BYTE
EXTRN score:BYTE

.CODE

EXTRN SET_CURSOR:PROC
EXTRN SHOW_GAME_OVER:PROC

PUBLIC MOVE_TARGET
PUBLIC CHECK_COLLISION
PUBLIC DRAW_TARGET
PUBLIC ERASE_TARGET
PUBLIC DRAW_SCORE
PUBLIC RANDOM_X

MOVE_TARGET PROC

    ADD targetY,2

    CMP targetY,24
    JGE TARGET_FAIL

    RET

TARGET_FAIL:

    JMP SHOW_GAME_OVER

MOVE_TARGET ENDP

CHECK_COLLISION PROC

    MOV AL,targetY
    CMP AL,playerY
    JNE NO_COLLISION

    MOV AL,targetX
    MOV BL,playerX

    DEC BL

    CMP AL,BL
    JL NO_COLLISION

    ADD BL,2

    CMP AL,BL
    JG NO_COLLISION

    INC score

    MOV targetY,4

    CALL RANDOM_X

NO_COLLISION:

    RET

CHECK_COLLISION ENDP

DRAW_TARGET PROC

    MOV DH,targetY
    MOV DL,targetX

    CALL SET_CURSOR

    MOV AH,09h
    MOV AL,254
    MOV BH,00h
    MOV BL,0Ch
    MOV CX,1

    INT 10h

    RET

DRAW_TARGET ENDP

ERASE_TARGET PROC

    MOV DH,targetY
    MOV DL,targetX

    CALL SET_CURSOR

    MOV DL,' '
    MOV AH,02h

    INT 21h

    RET

ERASE_TARGET ENDP

DRAW_SCORE PROC

    MOV DH,1
    MOV DL,18

    CALL SET_CURSOR

    XOR AX,AX
    MOV AL,score

    ADD AL,'0'

    MOV AH,09h
    MOV BH,00h
    MOV BL,0Ah
    MOV CX,1

    INT 10h

    RET

DRAW_SCORE ENDP

RANDOM_X PROC

    MOV AH,00h
    INT 1Ah

    MOV AX,DX

    XOR DX,DX

    MOV CX,74
    DIV CX

    ADD DL,3

    MOV targetX,DL

    RET

RANDOM_X ENDP

END
